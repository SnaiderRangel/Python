#Operadores

first_number = 10
second_number = 3


print( 'El resultado de la suma es: ', (first_number + second_number))
print( 'El resultado de la resta es: ', (first_number - second_number))
print( 'El resultado de la multiplicación es: ', (first_number * second_number))
print( 'El resultado de la división es: ', (first_number / second_number))
print( 'El resultado de la resto es: ', (first_number % second_number))
print( 'El resultado de la división aproximada  es: ', (first_number // second_number))
print( 'El resultado del numero elevado al cuadrado es: ', (first_number ** second_number))

# operadores comparativos

